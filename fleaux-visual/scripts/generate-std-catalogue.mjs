import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const STD_PATH = path.resolve(__dirname, '../../Std.fleaux');
const OUT_PATH = path.resolve(__dirname, '../src/lib/stdCatalogue.ts');

function splitTopLevel(input, delimiter) {
  const parts = [];
  let depth = 0;
  let token = '';
  for (const ch of input) {
    if (ch === '(') depth += 1;
    if (ch === ')') depth -= 1;
    if (ch === delimiter && depth === 0) {
      parts.push(token.trim());
      token = '';
      continue;
    }
    token += ch;
  }
  if (token.trim().length > 0) {
    parts.push(token.trim());
  }
  return parts;
}

function parseParam(raw) {
  const idx = raw.indexOf(':');
  if (idx === -1) {
    return { name: raw.trim(), type: 'Any' };
  }
  return {
    name: raw.slice(0, idx).trim(),
    type: raw.slice(idx + 1).trim(),
  };
}

function parseLetLine(line) {
  const trimmed = line.trim();
  if (!trimmed.startsWith('let ') || !trimmed.endsWith(';')) {
    return null;
  }

  const afterLet = trimmed.slice(4);
  const open = afterLet.indexOf('(');
  if (open === -1) {
    return null;
  }

  const qualifiedName = afterLet.slice(0, open).trim();
  let i = open + 1;
  let depth = 1;
  while (i < afterLet.length && depth > 0) {
    const ch = afterLet[i];
    if (ch === '(') depth += 1;
    if (ch === ')') depth -= 1;
    i += 1;
  }
  if (depth !== 0) {
    return null;
  }

  const paramsRaw = afterLet.slice(open + 1, i - 1).trim();
  const rest = afterLet.slice(i).trim();
  const retMatch = rest.match(/^:\s*(.*?)\s*::\s*(.*?)\s*;$/);
  if (!retMatch) {
    return null;
  }

  const params = paramsRaw.length === 0 ? [] : splitTopLevel(paramsRaw, ',').map(parseParam);
  const returnType = retMatch[1].trim();
  const body = retMatch[2].trim();

  return { qualifiedName, params, returnType, body };
}

function namespaceFrom(qualifiedName) {
  const parts = qualifiedName.split('.');
  if (parts.length <= 1) {
    return 'Std';
  }
  return parts.slice(0, -1).join('.');
}

function symbolFrom(qualifiedName) {
  const parts = qualifiedName.split('.');
  return parts[parts.length - 1] ?? qualifiedName;
}

const src = readFileSync(STD_PATH, 'utf8');
const lines = src.split(/\r?\n/);

const stdValues = [];
const stdFunctions = [];
const namespaceOrder = [];
const seenNs = new Set();

for (const line of lines) {
  const parsed = parseLetLine(line);
  if (!parsed) {
    continue;
  }
  if (!parsed.qualifiedName.startsWith('Std.')) {
    continue;
  }

  const namespace = namespaceFrom(parsed.qualifiedName);
  const name = symbolFrom(parsed.qualifiedName);

  if (!seenNs.has(namespace)) {
    seenNs.add(namespace);
    namespaceOrder.push(namespace);
  }

  if (parsed.body === '__builtin__') {
    stdFunctions.push({
      qualifiedName: parsed.qualifiedName,
      namespace,
      name,
      params: parsed.params,
      returnType: parsed.returnType,
    });
  } else if (parsed.params.length === 0) {
    stdValues.push({
      qualifiedName: parsed.qualifiedName,
      namespace,
      name,
      valueType: parsed.returnType,
    });
  }
}

const output = `// AUTO-GENERATED by scripts/generate-std-catalogue.mjs from ../Std.fleaux\n// Do not edit by hand.\n\nexport type StdParam = { name: string; type: string };\n\nexport type StdValueEntry = {\n  qualifiedName: string;\n  namespace: string;\n  name: string;\n  valueType: string;\n};\n\nexport type StdFunctionEntry = {\n  qualifiedName: string;\n  namespace: string;\n  name: string;\n  params: StdParam[];\n  returnType: string;\n};\n\nexport const STD_VALUES: StdValueEntry[] = ${JSON.stringify(stdValues, null, 2)};\n\nexport const STD_FUNCTIONS: StdFunctionEntry[] = ${JSON.stringify(stdFunctions, null, 2)};\n\nexport const STD_VALUES_BY_NAMESPACE: Map<string, StdValueEntry[]> = (() => {\n  const map = new Map<string, StdValueEntry[]>();\n  for (const value of STD_VALUES) {\n    const group = map.get(value.namespace) ?? [];\n    group.push(value);\n    map.set(value.namespace, group);\n  }\n  return map;\n})();\n\nexport const STD_BY_NAMESPACE: Map<string, StdFunctionEntry[]> = (() => {\n  const map = new Map<string, StdFunctionEntry[]>();\n  for (const fn of STD_FUNCTIONS) {\n    const group = map.get(fn.namespace) ?? [];\n    group.push(fn);\n    map.set(fn.namespace, group);\n  }\n  return map;\n})();\n\nexport const STD_NAMESPACES = ${JSON.stringify(namespaceOrder, null, 2)} as const;\n`;

mkdirSync(path.dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, output);
console.log(`Wrote ${OUT_PATH}`);

